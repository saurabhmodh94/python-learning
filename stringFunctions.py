# STRING FUNCTIONS

mystr = "Hello World"

print(mystr.capitalize())

print(mystr.swapcase())

print(len(mystr))

print(mystr.replace("World", "Everyone"))

sub="o"
print(mystr.count(sub))

print(mystr.startswith("Hello"))

print(mystr.endswith("World"))

print(mystr.split())

print(mystr.find("W")) # returns -1

print(mystr.index("W")) # throws error

print(mystr.isalnum())

print(mystr.isalpha())

print(mystr.isdigit())